// Global Translation Helper Logic
function applyTranslations(lang) {
  // Save selection
  localStorage.setItem('preferred-language', lang);
  document.documentElement.lang = lang;

  // 1. Text elements translations
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });

  // 2. Placeholder translations
  const placeholders = document.querySelectorAll('[data-i18n-placeholder]');
  placeholders.forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang] && translations[lang][key]) {
      el.setAttribute('placeholder', translations[lang][key]);
    }
  });

  // 3. Update Language Switcher UI Active state
  const langButtons = document.querySelectorAll('.lang-btn');
  langButtons.forEach(btn => {
    const btnLang = btn.getAttribute('data-lang');
    if (btnLang === lang) {
      btn.classList.add('text-elegant-gold', 'font-bold');
      btn.classList.remove('text-on-surface-variant', 'dark:text-surface-variant', 'text-stone-gray/60');
    } else {
      btn.classList.remove('text-elegant-gold', 'font-bold');
      btn.classList.add('text-on-surface-variant', 'dark:text-surface-variant', 'text-stone-gray/60');
    }
  });
}

function setLanguage(lang) {
  if (translations[lang]) {
    applyTranslations(lang);
  }
}

// On load, apply language
document.addEventListener('DOMContentLoaded', () => {
  const preferredLang = localStorage.getItem('preferred-language') || 'tr';
  applyTranslations(preferredLang);
});
